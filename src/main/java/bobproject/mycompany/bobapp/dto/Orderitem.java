package bobproject.mycompany.bobapp.dto;

public class Orderitem {
	private int ono;
	private int pno;
	private int opqn;
	public int getOno() {
		return ono;
	}
	public void setOno(int ono) {
		this.ono = ono;
	}
	public int getPno() {
		return pno;
	}
	public void setPno(int pno) {
		this.pno = pno;
	}
	public int getOpqn() {
		return opqn;
	}
	public void setOpqn(int opqn) {
		this.opqn = opqn;
	}
}
